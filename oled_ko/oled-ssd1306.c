static int oled_probe(struct i2c_client *client,
                         const struct i2c_device_id *id)
{
    int ret;

    printk("%s enter.\n", __func__);
    i2c_client=client;
    oled_dev_init();
    //OLED_Fill(0xff);
    OLED_Fill(0x00);

    ret = sysfs_create_bin_file(&client->dev.kobj, &oled_attr);
        if (ret)
                dev_err(&client->dev, "Unable to create sysfs entry: %s\n",
                        oled_attr.attr.name);

    return 0;
}

static int oled_remove(struct i2c_client *client)
{
    sysfs_remove_bin_file(&client->dev.kobj, &oled_attr);
    return 0;
}

